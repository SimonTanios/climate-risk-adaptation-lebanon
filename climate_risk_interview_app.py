def discover_output_roots():
    roots = []

    # 1) Prefer the known folders from your climate-risk work.
    for folder in KNOWN_OUTPUT_FOLDERS:
        p = BASE_DIR / folder
        if p.exists() and p.is_dir() and not is_excluded_path(p):
            roots.append(p)

    # 2) Auto-detect additional relevant climate/Lebanon folders, but exclude unrelated folders.
    allowed_keywords = ["lebanon", "climate", "drought", "risk", "hazard", "irrigation", "flood", "cmip6", "vegetation", "erosion", "runoff", "evapotranspiration"]
    for p in BASE_DIR.iterdir():
        if not p.is_dir():
            continue
        if is_excluded_path(p):
            continue
        lower_name = p.name.lower()
        if not any(k in lower_name for k in allowed_keywords):
            continue
        has_outputs = any(p.rglob("*.html")) or any(p.rglob("*.csv")) or any(p.rglob("*.xlsx")) or any(p.rglob("*.png"))
        if has_outputs and p not in roots:
            roots.append(p)

    return sorted(roots, key=lambda x: x.name.lower())


def discover_files():
    roots = discover_output_roots()
    records = []
    for root in roots:
        for pattern in ("*.html", "*.csv", "*.xlsx", "*.png", "*.jpg", "*.jpeg"):
            for f in root.rglob(pattern):
                if f.name.startswith("~$") or is_excluded_path(f):
                    continue
                rel = f.relative_to(BASE_DIR)
                records.append({
                    "path": f,
                    "relative_path": str(rel).replace("\\", "/"),
                    "name": clean_name(f.name),
                    "file_name": f.name,
                    "folder": root.name,
                    "theme": infer_theme(f),
                    "suffix": f.suffix.lower(),
                    "modified": datetime.fromtimestamp(f.stat().st_mtime).strftime("%Y-%m-%d %H:%M"),
                    "size_kb": round(f.stat().st_size / 1024, 1),
                })
    return records


