def find_featured_maps():
    featured = []
    seen = set()
    roots = discover_output_roots()
    for pattern in FEATURED_PATTERNS:
        for root in roots:
            for f in root.rglob(pattern):
                if is_excluded_path(f):
                    continue
                rel = str(f.relative_to(BASE_DIR)).replace("\\", "/")
                if rel in seen:
                    continue
                rec = get_record(rel)
                if rec:
                    featured.append(rec)
                    seen.add(rel)
    if not featured:
        featured = MAP_FILES[:8]
    return featured[:16]


