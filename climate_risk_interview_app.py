@app.callback(
    Output("image-output", "children"),
    Input("image-select", "value"),
)
def load_image(relative_path):
    if not relative_path:
        return dbc.Alert("No figures/images were found.", color="warning")
    rec = get_record(relative_path)
    if not rec:
        return dbc.Alert("Selected image not found.", color="danger")

    import base64
    mime = "image/png" if rec["suffix"] == ".png" else "image/jpeg"
    data = base64.b64encode(rec["path"].read_bytes()).decode("ascii")
    src = f"data:{mime};base64,{data}"

    return html.Div([
        html.Div([
            dbc.Badge(rec["theme"], color="primary", className="me-2"),
            html.Strong(rec["name"]),
            html.Div(rec["relative_path"], className="text-muted small mt-1")
        ], className="mb-3"),
        html.Img(src=src, style={"maxWidth": "100%", "border": "1px solid #ddd", "borderRadius": "8px"})
    ])

