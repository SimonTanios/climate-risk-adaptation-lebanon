def is_excluded_path(path: Path) -> bool:
    parts = {p.lower() for p in path.parts}
    excluded = {x.lower() for x in EXCLUDED_FOLDERS}
    if parts.intersection(excluded):
        return True
    path_text = str(path).lower()
    if "output_water_detection" in path_text:
        return True
    return False


def clean_name(path_or_name):
    name = Path(path_or_name).stem if isinstance(path_or_name, (str, Path)) else str(path_or_name)
    name = re.sub(r"[_\-]+", " ", name)
    name = re.sub(r"\bmap\b", "Map", name, flags=re.IGNORECASE)
    name = re.sub(r"\bssp245\b", "SSP2-4.5", name, flags=re.IGNORECASE)
    name = re.sub(r"\bssp585\b", "SSP5-8.5", name, flags=re.IGNORECASE)
    name = re.sub(r"\s+", " ", name).strip()
    return name.title()


