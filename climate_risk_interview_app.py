def infer_numeric_columns(df: pd.DataFrame):
    numeric_cols = []
    for c in df.columns:
        s = pd.to_numeric(df[c], errors="coerce")
        if s.notna().sum() >= max(1, min(5, len(df) // 5)):
            numeric_cols.append(c)
    return numeric_cols


def infer_label_columns(df: pd.DataFrame):
    preferred = [
        "class_label", "hazard_label", "risk_label", "recommended_intervention",
        "scenario", "period", "month", "HYBAS_ID", "flood_class",
        "vulnerability_class", "irrigation_priority_class", "csa_code",
        "climate_stress_class", "et_stress_class", "vci_drought_class"
    ]
    labels = [c for c in preferred if c in df.columns]
    labels += [c for c in df.columns if df[c].dtype == "object" and c not in labels]
    labels += [c for c in df.columns if c not in labels]
    return labels


def build_chart(df: pd.DataFrame, selected_x=None, selected_y=None):
    if df.empty or len(df.columns) < 2:
        return html.Div()

    numeric_cols = infer_numeric_columns(df)
    label_cols = infer_label_columns(df)
    if not numeric_cols:
        return dbc.Alert("No numeric column detected for charting.", color="secondary")

    x_col = selected_x if selected_x in df.columns else label_cols[0]
    y_col = selected_y if selected_y in df.columns else numeric_cols[0]

    tmp = df.copy()
    tmp[y_col] = pd.to_numeric(tmp[y_col], errors="coerce")
    tmp = tmp.dropna(subset=[y_col]).head(100)

    if tmp.empty:
        return dbc.Alert("No numeric rows available for the selected chart.", color="secondary")

    fig = px.bar(
        tmp,
        x=x_col,
        y=y_col,
        title=f"{y_col} by {x_col}",
        template="plotly_white"
    )
    fig.update_layout(margin=dict(l=30, r=20, t=60, b=80), height=420)
    return dcc.Graph(figure=fig, config={"displaylogo": False})


def read_html_map(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8", errors="ignore")
    except Exception as e:
        return f"<h3>Could not read map</h3><p>{html_lib.escape(str(e))}</p>"


