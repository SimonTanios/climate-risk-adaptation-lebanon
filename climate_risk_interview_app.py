def load_map(relative_path):
    if not relative_path:
        return "<h3>No map selected</h3>", dbc.Alert("No HTML maps were found. Check that the app is in the same folder as your output folders.", color="warning")

    rec = get_record(relative_path)
    if not rec:
        return "<h3>Map not found</h3>", dbc.Alert("Selected map not found.", color="danger")

    src_doc = read_html_map(rec["path"])
    meta = dbc.Row([
        dbc.Col(dbc.Badge(rec["theme"], color="primary"), width="auto"),
        dbc.Col(html.Strong(rec["name"]), width="auto"),
        dbc.Col(html.Span(rec["relative_path"], className="text-muted small"), width="auto"),
    ], align="center", className="g-2")
    return src_doc, meta


@app.callback(
    Output("table-output", "children"),
    Output("table-metadata", "children"),
    Output("chart-x", "options"),
    Output("chart-y", "options"),
    Output("chart-x", "value"),
    Output("chart-y", "value"),
    Input("table-select", "value"),
    Input("table-search", "value"),
)
def load_table(relative_path, search_text):
    if not relative_path:
        return dbc.Alert("No CSV/Excel tables were found.", color="warning"), "", [], [], None, None

    rec = get_record(relative_path)
    if not rec:
        return dbc.Alert("Selected table not found.", color="danger"), "", [], [], None, None

    df = safe_read_table(rec["path"])

    if search_text and not df.empty:
        mask = pd.Series(False, index=df.index)
        for col in df.columns:
            mask = mask | df[col].astype(str).str.contains(search_text, case=False, na=False)
        df = df[mask]

    numeric_cols = infer_numeric_columns(df)
    label_cols = infer_label_columns(df)
    all_options = [{"label": str(c), "value": str(c)} for c in df.columns]
    num_options = [{"label": str(c), "value": str(c)} for c in numeric_cols]

    x_value = label_cols[0] if label_cols else (df.columns[0] if len(df.columns) else None)
    y_value = numeric_cols[0] if numeric_cols else None

    metadata = html.Div([
        dbc.Badge(rec["theme"], color="primary", className="me-2"),
        html.Strong(rec["name"]),
        html.Div(f"{rec['relative_path']} | {len(df):,} rows | {len(df.columns):,} columns", className="text-muted small mt-1")
    ])

    return make_table_component(df), metadata, all_options, num_options, x_value, y_value


